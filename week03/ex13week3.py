import  turtle
turtle.shape("turtle")
turtle.begin_fill()
for i in range(100):
    turtle.forward(10)
    turtle.left(180-180*(100-2)/100)
turtle.color('yellow')
turtle.end_fill()
turtle.penup()
turtle.color('black')
turtle.goto(-45, 230)
turtle.pendown()
turtle.begin_fill()
for i in range(100):
    turtle.forward(1)
    turtle.left(180-180*(100-2)/100)
turtle.color('blue')
turtle.end_fill()
turtle.penup()
turtle.color('black')
turtle.goto(45, 230)
turtle.pendown()
turtle.begin_fill()
for i in range(100):
    turtle.forward(1)
    turtle.left(180-180*(100-2)/100)
turtle.color('blue')
turtle.end_fill()
turtle.penup()
turtle.color('black')
turtle.goto(0, 180)
turtle.pendown()
turtle.right(90)
turtle.width(10)
turtle.forward(50)
turtle.penup()
turtle.color('black')
turtle.goto(65, 130)
turtle.pendown()
turtle.color('red')
for i in range(50):
    turtle.forward(4)
    turtle.right(180 - 180 * (100 - 2) / 100)
